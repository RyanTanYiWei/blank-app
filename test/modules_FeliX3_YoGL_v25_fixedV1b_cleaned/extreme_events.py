@component.add(
    name="Random Normal Arrivals",
    units="Events/Year",
    comp_type="Auxiliary",
    comp_subtype="Normal",
    depends_on={
        "extreme_event_mean": 1,
        "pi": 1,
        "randomlookup2": 1,
        "fixed_std_dev": 1,
        "randomlookup3": 1,
        "minimum_number_of_extreme_events": 1,
    },
)
def random_normal_arrivals():
    """
    MAX( Extreme Event Mean + Std Dev * SQRT(-2 * LN(RandomLookUp2)) * cos(2 * PI * RandomLookUp1) , Minimum Number of Extreme Events)
    """
    return float(
        np.maximum(
            extreme_event_mean()
            + fixed_std_dev()
            * float(np.sqrt(-2 * float(np.log(randomlookup3()))))
            * float(np.cos(2 * pi() * randomlookup2())),
            minimum_number_of_extreme_events(),
        )
    )


